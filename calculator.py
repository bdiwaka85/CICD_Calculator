# This function adds two numbers 
def add(x, y):
    return x + y
    
# This function subtracts two numbers 
def subtract(x, y):
    return x - y
    
# This function multiplies two numbers
def multiply(x, y):
    return x * y
    
# This function divides two numbers
def divide(x, y):
    return x / y

# This function divides two numbers
def exponent(x, y):
    return x ^ y 
